function click_installer() {
   global $wpdb;


   $table_name = $wpdb->prefix . "click_settings";

	if($wpdb->get_var("SHOW TABLES LIKE '$table_name'") != $table_name) {

	$sql = "CREATE TABLE " . $table_name . " ( id mediumint(9) NOT NULL AUTO_INCREMENT, backup_full varchar(64) NOT NULL, auto_download varchar(64) NOT NULL, reg_key varchar(64) NOT NULL, mail_to varchar(64) NOT NULL, mail_auto varchar(64) NOT NULL, mail_manual varchar(64) NOT NULL, UNIQUE KEY id (id))";

